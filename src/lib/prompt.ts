export function buildUserPrompt(form: TravelFormData): string {
  const dateBlock =
    form.dateFrom && form.dateTo
      ? `- Даты поездки: с ${form.dateFrom} по ${form.dateTo} (учти в day_plan.date)`
      : form.dateFrom
      ? `- Дата отправления: ${form.dateFrom}`
      : "- Конкретные даты не заданы (day_plan.date можно опустить)";

  return `Составь 3 варианта маршрута путешествия со следующими параметрами:

- Бюджет: ${form.budget.toLocaleString("ru")} ₽ на всю поездку (на всех)
- Количество дней: ${form.days}
- Откуда едем: ${form.startCity}
- Дальность: ${DISTANCE_MAP[form.distancePreference]}
- Стиль поездки: ${STYLE_MAP[form.tripStyle]}
- Количество человек: ${form.peopleCount}
- Приоритет: ${PRIORITY_MAP[form.priority]}
${dateBlock}
${form.constraints ? `- Ограничения и пожелания: ${form.constraints}` : "- Особых ограничений нет"}

Помни: нужно РОВНО 3 варианта (budget / balanced / comfort). Верни ТОЛЬКО JSON.`;
}
